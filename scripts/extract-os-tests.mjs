#!/usr/bin/env node
/**
 * extract-os-tests.mjs
 *
 * Scans the OS Examen folder in wiki/raw/, sends each test unit (single PDF or
 * folder-of-PDFs) to Gemini, extracts structured JSON matching the OS test
 * schema, and saves to src/content/os/tests/.
 *
 * Usage:
 *   node scripts/extract-os-tests.mjs                           # process all
 *   node scripts/extract-os-tests.mjs --dry-run                 # list units only, no Gemini calls
 *   node scripts/extract-os-tests.mjs --folder "TP1_2024/varianta_2"
 *   node scripts/extract-os-tests.mjs --force                   # overwrite existing JSON
 */

import { readFileSync, writeFileSync, readdirSync, statSync, existsSync, mkdirSync, copyFileSync } from 'fs';
import { resolve, join, basename, extname } from 'path';
import { GoogleGenerativeAI } from '@google/generative-ai';

// ── Config ──

const EXAMEN_ROOT = resolve('wiki/raw/other/prev-year-so/Sisteme de Operare/Examen');
const OUTPUT_DIR = resolve('src/content/os/tests');
const SOURCE_DIR = resolve('src/content/os/tests/source');
const PROMPT_PATH = resolve('scripts/prompts/extract-os-test.md');

const SUPPORTED_PDF = new Set(['.pdf']);

// PDFs that are reference material (not tests) — never extract.
const EXCLUDED_FILENAMES = new Set([
  'Cheat-Sheets.pdf',
  'regular-expressions.pdf',
  'OS_Exam.pdf',
  'test 1 lab 2021 raspunsuri corectat final final maybe.pdf', // answer key, not a test
]);

// Slugs that are hand-curated and should never be regenerated by this script.
const PROTECTED_SLUGS = new Set([
  'tp1-2023-2024-v1',                                   // hand-authored existing
  'examen2023-2024-tp1-2024-varianta-1',                // would-be duplicate of above
]);

// ── Load keys from proxy/.env ──

function loadEnv() {
  const envPath = resolve('proxy/.env');
  if (!existsSync(envPath)) {
    console.error('ERROR: proxy/.env not found. Need Gemini API keys.');
    process.exit(1);
  }
  const raw = readFileSync(envPath, 'utf-8');
  const vars = {};
  raw.split('\n').forEach(line => {
    const m = line.match(/^([^#=]+)=(.*)$/);
    if (m) vars[m[1].trim()] = m[2].trim();
  });
  return vars;
}

const env = loadEnv();
const geminiKeys = (env.GEMINI_API_KEYS || '').split(',').map(k => k.trim()).filter(Boolean);
if (geminiKeys.length === 0) {
  console.error('ERROR: No GEMINI_API_KEYS in proxy/.env');
  process.exit(1);
}
console.log(`Loaded ${geminiKeys.length} Gemini key(s)`);

let keyIndex = 0;
function getClient() {
  const key = geminiKeys[keyIndex % geminiKeys.length];
  keyIndex++;
  return new GoogleGenerativeAI(key);
}

const openrouterKeys = (env.OPENROUTER_API_KEYS || '').split(',').map(k => k.trim()).filter(Boolean);
let orKeyIndex = 0;
console.log(`Loaded ${openrouterKeys.length} OpenRouter key(s)`);

const GEMINI_MODELS = ['gemini-3-flash-preview'];

// ── Gemini call with retry + OpenRouter fallback ──

async function callGemini(parts) {
  for (const modelName of GEMINI_MODELS) {
    for (let ki = 0; ki < geminiKeys.length; ki++) {
      const client = getClient();
      const model = client.getGenerativeModel({ model: modelName });
      try {
        const result = await model.generateContent(parts);
        return result.response.text();
      } catch (err) {
        const isRateLimit = err.status === 429 || err.message?.includes('429') || err.message?.includes('RESOURCE_EXHAUSTED');
        const isUnavailable = err.status === 503 || err.message?.includes('503');
        if (isRateLimit || isUnavailable) {
          console.log(`    Gemini ${modelName} key ${ki + 1}: ${isRateLimit ? 'rate limited' : 'unavailable'}`);
          continue;
        }
        throw err;
      }
    }
  }

  if (openrouterKeys.length > 0) {
    console.log(`    Falling back to OpenRouter...`);
    return callOpenRouter(parts);
  }

  throw new Error('All Gemini keys exhausted and no OpenRouter keys available');
}

async function callOpenRouter(parts) {
  const content = [];
  for (const p of parts) {
    if (p.text) {
      content.push({ type: 'text', text: p.text });
    } else if (p.inlineData) {
      const { mimeType, data } = p.inlineData;
      content.push({
        type: 'image_url',
        image_url: { url: `data:${mimeType};base64,${data}` },
      });
    }
  }

  const key = openrouterKeys[orKeyIndex % openrouterKeys.length];
  orKeyIndex++;

  const res = await fetch('https://openrouter.ai/api/v1/chat/completions', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${key}`,
      'Content-Type': 'application/json',
      'HTTP-Referer': 'https://corgigh.github.io/Ghid_Studii_AI/',
    },
    body: JSON.stringify({
      model: 'google/gemini-2.5-flash',
      messages: [{ role: 'user', content }],
      max_tokens: 16000,
    }),
  });

  if (!res.ok) {
    const body = await res.text();
    throw new Error(`OpenRouter ${res.status}: ${body.slice(0, 300)}`);
  }

  const data = await res.json();
  return data.choices?.[0]?.message?.content || '';
}

// ── Discover test units ──
//
// A "test unit" is one conceptual test → one output JSON.
// Rules:
//   - Folder with >= 2 PDFs where names look like variants (varianta_*, part_*,
//     partea *, PartialSOT*, SO T2 *) → split each PDF into its own unit.
//   - Folder with a small number of tightly-coupled PDFs (e.g. specificatie + barem)
//     → keep folder as single unit with all PDFs.
//   - Single PDF in its own folder → folder is the unit.
//   - Excluded filenames are always dropped from consideration.

const VARIANT_FILENAME_PATTERNS = [
  /^varianta[_\s]?\d+\.pdf$/i,
  /^part[_\s]?\d+\.pdf$/i,
  /^partea[_\s]?\d+\.pdf$/i,
  /^PartialSOT\d+[-_].*\.pdf$/i,
  /^SO T\d+ \d+\.pdf$/i,
  /^TP\d+_var\d+\.pdf$/i,
];

function looksLikeVariantFilename(name) {
  return VARIANT_FILENAME_PATTERNS.some(p => p.test(name));
}

function discoverUnits(root) {
  const units = [];

  function scan(dir, breadcrumb) {
    const entries = readdirSync(dir);
    const files = [];
    const subdirs = [];

    for (const name of entries) {
      const full = join(dir, name);
      const stat = statSync(full);
      if (stat.isDirectory()) {
        subdirs.push({ name, full });
      } else if (SUPPORTED_PDF.has(extname(name).toLowerCase())) {
        if (EXCLUDED_FILENAMES.has(name)) continue;
        files.push({ name, full });
      }
    }

    if (files.length > 0) {
      const allLookVariant = files.every(f => looksLikeVariantFilename(f.name));

      if (files.length >= 2 && allLookVariant) {
        // Folder of sibling variants → split each PDF into its own unit
        for (const f of files) {
          units.push({
            breadcrumb: [...breadcrumb, basename(f.name, '.pdf')],
            files: [f],
          });
        }
      } else if (files.length === 1 && looksLikeVariantFilename(files[0].name)) {
        // Lone variant-named PDF → extend breadcrumb with stem for informative slug
        units.push({
          breadcrumb: [...(breadcrumb.length > 0 ? breadcrumb : [basename(dir)]), basename(files[0].name, '.pdf')],
          files,
        });
      } else {
        // Single unit: a tightly-coupled set (spec+barem) or lone non-variant PDF
        units.push({
          breadcrumb: breadcrumb.length > 0 ? breadcrumb : [basename(dir)],
          files,
        });
      }
    }

    for (const sub of subdirs) {
      scan(sub.full, [...breadcrumb, sub.name]);
    }
  }

  scan(root, []);
  return units;
}

// ── Slug generation ──

function makeSlug(breadcrumb) {
  const joined = breadcrumb.join('-').toLowerCase();
  return joined
    .replace(/[àáâãäå]/g, 'a')
    .replace(/[èéêë]/g, 'e')
    .replace(/[ìíîï]/g, 'i')
    .replace(/[òóôõö]/g, 'o')
    .replace(/[ùúûü]/g, 'u')
    .replace(/[șş]/g, 's')
    .replace(/[țţ]/g, 't')
    .replace(/examen_(\d{4})-(\d{4})/g, 'examen$1-$2')
    .replace(/\s+/g, '-')
    .replace(/[()]/g, '')
    .replace(/[^a-z0-9-]/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
}

// ── Build Gemini parts ──

function buildParts(unit, promptText) {
  const parts = [];
  // Sort: specificatie before barem, else alphabetical
  const sorted = [...unit.files].sort((a, b) => {
    const ra = /barem/i.test(a.name) ? 2 : /specificatie/i.test(a.name) ? 0 : 1;
    const rb = /barem/i.test(b.name) ? 2 : /specificatie/i.test(b.name) ? 0 : 1;
    if (ra !== rb) return ra - rb;
    return a.name.localeCompare(b.name);
  });

  for (const pdf of sorted) {
    const data = readFileSync(pdf.full).toString('base64');
    parts.push({ inlineData: { mimeType: 'application/pdf', data } });
  }
  parts.push({ text: promptText });
  return parts;
}

// ── Parse Gemini JSON ──

function parseResponse(raw) {
  let cleaned = raw.trim();
  if (cleaned.startsWith('```')) {
    cleaned = cleaned.replace(/^```(?:json)?\s*\n?/, '').replace(/\n?```\s*$/, '');
  }
  // Models sometimes append prose after the JSON object. Find the first
  // balanced top-level {...} and parse only that.
  const start = cleaned.indexOf('{');
  if (start === -1) throw new Error('No JSON object found in response');
  let depth = 0, inString = false, escape = false;
  for (let i = start; i < cleaned.length; i++) {
    const c = cleaned[i];
    if (escape) { escape = false; continue; }
    if (c === '\\' && inString) { escape = true; continue; }
    if (c === '"') inString = !inString;
    else if (!inString) {
      if (c === '{') depth++;
      else if (c === '}') {
        depth--;
        if (depth === 0) {
          const slice = cleaned.slice(start, i + 1);
          try {
            return JSON.parse(slice);
          } catch (err) {
            // LLMs sometimes emit invalid JSON escapes (\c, \x, \(, etc.).
            // Escape any backslash not followed by a valid JSON escape char.
            const repaired = slice.replace(/\\(?!["\\\/bfnrtu])/g, '\\\\');
            return JSON.parse(repaired);
          }
        }
      }
    }
  }
  throw new Error('Unterminated JSON object in response');
}

// ── Finalize extracted JSON (inject slug, sanity-check) ──

function finalize(extracted, slug) {
  if (!extracted || typeof extracted !== 'object') {
    throw new Error('Gemini response is not an object');
  }
  if (!extracted.meta || !Array.isArray(extracted.questions)) {
    throw new Error('Missing meta or questions[]');
  }
  extracted.meta.id = `os-${slug}`;
  if (extracted.questions.length === 0) {
    throw new Error('questions[] is empty');
  }
  return extracted;
}

// ── Main ──

async function main() {
  const args = process.argv.slice(2);
  const dryRun = args.includes('--dry-run');
  const force = args.includes('--force');
  const folderFilter = args.includes('--folder') ? args[args.indexOf('--folder') + 1] : null;

  if (!existsSync(EXAMEN_ROOT)) {
    console.error(`ERROR: Examen root not found: ${EXAMEN_ROOT}`);
    process.exit(1);
  }
  if (!existsSync(OUTPUT_DIR)) mkdirSync(OUTPUT_DIR, { recursive: true });
  if (!existsSync(SOURCE_DIR)) mkdirSync(SOURCE_DIR, { recursive: true });

  const promptText = readFileSync(PROMPT_PATH, 'utf-8');
  const allUnits = discoverUnits(EXAMEN_ROOT);

  const units = folderFilter
    ? allUnits.filter(u => u.breadcrumb.join('/').toLowerCase().includes(folderFilter.toLowerCase()))
    : allUnits;

  console.log(`\nDiscovered ${allUnits.length} total unit(s); ${units.length} after filter.\n`);

  let ok = 0, skipped = 0, failed = 0;

  for (const unit of units) {
    const trail = unit.breadcrumb.join(' > ');
    const slug = makeSlug(unit.breadcrumb);
    const fileNames = unit.files.map(f => f.name).join(', ');

    console.log(`  ${trail}`);
    console.log(`    slug: ${slug} | files: [${fileNames}]`);

    if (PROTECTED_SLUGS.has(slug)) {
      console.log(`    SKIP (protected slug — hand-curated)`);
      skipped++;
      continue;
    }

    if (dryRun) continue;

    const outPath = join(OUTPUT_DIR, `${slug}.json`);
    if (existsSync(outPath) && !force) {
      console.log(`    SKIP (already exists; use --force to overwrite)`);
      skipped++;
      continue;
    }

    try {
      console.log(`    Sending ${unit.files.length} PDF(s) to Gemini...`);
      const parts = buildParts(unit, promptText);
      const raw = await callGemini(parts);
      const extracted = parseResponse(raw);
      const finalJson = finalize(extracted, slug);

      writeFileSync(outPath, JSON.stringify(finalJson, null, 2));
      console.log(`    Saved: ${slug}.json (${finalJson.meta.totalPoints || '?'}pts, ${finalJson.questions.length}q)`);

      // Copy sources
      const sourceSubdir = join(SOURCE_DIR, slug);
      if (!existsSync(sourceSubdir)) mkdirSync(sourceSubdir, { recursive: true });
      for (const f of unit.files) {
        copyFileSync(f.full, join(sourceSubdir, f.name));
      }

      ok++;
      await new Promise(r => setTimeout(r, 2000));
    } catch (err) {
      console.error(`    ERROR: ${err.message}`);
      writeFileSync(
        join(OUTPUT_DIR, `${slug}.error.txt`),
        `${err.message}\n\n${err.stack || ''}`
      );
      failed++;
    }
  }

  console.log(`\nDone. ok=${ok} skipped=${skipped} failed=${failed}`);

  if (!dryRun && ok > 0) {
    console.log('\n=== INDEX.JS ENTRIES (paste into src/content/os/index.js tests[]) ===\n');
    const jsons = readdirSync(OUTPUT_DIR)
      .filter(f => f.endsWith('.json') && !f.includes('.error'));
    for (const f of jsons.sort()) {
      try {
        const data = JSON.parse(readFileSync(join(OUTPUT_DIR, f), 'utf-8'));
        const m = data.meta;
        const id = m.id || f.replace(/\.json$/, '');
        const titleEn = (m.title?.en || id).replace(/'/g, "\\'");
        const titleRo = (m.title?.ro || id).replace(/'/g, "\\'");
        console.log(`    { id: '${id}', title: { en: '${titleEn}', ro: '${titleRo}' }, shortTitle: { en: '${id}', ro: '${id}' }, src: 'os/tests/${f}' },`);
      } catch { /* skip malformed */ }
    }
  }
}

main().catch(err => {
  console.error('Fatal:', err);
  process.exit(1);
});
